    private void tabelEkspedisiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelEkspedisiMouseClicked
        // TODO add your handling code here:
        int baris = tabelEkspedisi.rowAtPoint(evt.getPoint());
        String id = tabelEkspedisi.getValueAt(baris, 0).toString();
        txtKode.setText(id);
        
        String nama = tabelEkspedisi.getValueAt(baris, 1).toString();
        txtNama.setText(nama);
        
        String barang = tabelEkspedisi.getValueAt(baris, 2).toString();
        txtBarang.setText(barang);
        
        String berat = tabelEkspedisi.getValueAt(baris, 3).toString();
        cmbBerat.setSelectedItem(berat);
        
        String jenis_barang = tabelEkspedisi.getValueAt(baris, 4).toString();
        cmbBarang.setSelectedItem(jenis_barang);
        
        String kota_tujuan = tabelEkspedisi.getValueAt(baris, 5).toString();
        cmbKota.setSelectedItem(kota_tujuan);
        
        String kec_tujuan = tabelEkspedisi.getValueAt(baris, 6).toString();
        cmbKec.setSelectedItem(kec_tujuan);
        
        String alamat = tabelEkspedisi.getValueAt(baris, 7).toString();
        txtAlamat.setText(alamat);
        
        String jenis_layanan = tabelEkspedisi.getValueAt(baris, 8).toString();
        cmbLayanan.setSelectedItem(jenis_layanan);
        
        String harga = tabelEkspedisi.getValueAt(baris, 9).toString();
        txtBayar.setText(harga);
        
        String status = tabelEkspedisi.getValueAt(baris, 10).toString();
        cmbStatus.setSelectedItem(status);
        
    }//GEN-LAST:event_tabelEkspedisiMouseClicked

