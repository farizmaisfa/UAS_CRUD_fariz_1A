    private void tampil(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Pengiriman");
        model.addColumn("Nama Pengirim");
        model.addColumn("Nama Barang");
        model.addColumn("Berat Barang");
        model.addColumn("Jenis Barang");
        model.addColumn("Kota Tujuan");
        model.addColumn("Kecamatan Tujuan");
        model.addColumn("Alamat");
        model.addColumn("Jenis Layanan");
        model.addColumn("Harga");
        model.addColumn("Status");
        
        try{
            String sql = "SELECT * FROM admin";
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10), res.getString(11)});
            }
            tabelEkspedisi.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error: " +e.getMessage());
        }
    }

